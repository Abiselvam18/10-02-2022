
public class Arithmetic {

	public static void main(String[] args) {
	
		int a=10;
		int b=11;
		int c=12;
		
		int add=a+b+c;
		int sub=c-a;
		int mul=a*b;
		int div=c/3;
		int rem=c%a;
		
		System.out.println(add);
		System.out.println(sub);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(rem);

	}

}
